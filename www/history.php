			<h1>History</h1>
			<p>
				St. Mary&#8217;s Parish was founded by a small group of Episcopal women in 1886. It was built, in 1887, on land donated by the Pacific Improvement Company and was the first church building constructed in Pacific Grove. The architect, William Hamilton, of Sacramento, modeled St. Mary&#8217;s after a church in Bath, England. On July 10, 1887, The Rt. Reverend Bishop Kip, the first Episcopal Bishop of California, presided at the dedication of what came to be known as the &#8220;little red church that cares.&#8221;
			</p>
			<p>
				The church was significantly enlarged in 1911 by splitting it in half and adding the choir and the first two bays. This enlargement was based on a plan devised by Lewis P. Hobart, the architect of Grace Cathedral in San Francisco. The porch entry was also added. The parish hall was constructed in 1893 and dedicated in 1991 to the Reverend Dwight Edwards, Rector for 25 years before his retirement in 1998. The education and office building, named after Rev. D. Albert Clay, Rector from 1926-1936, was erected in 1965 on the site of the former rectory. Both the church and Edwards Hall are constructed of No. 1 grade redwood no longer available at any price today. The church interior also includes pine, cedar, fir and walnut. The chancel rail is of oak and the bas relief reredos and the memorial altar is camphor wood. The church bell, one of the first gifts to the parish, was donated before the church itself was built.
			</p>
			<p>
				The present organ, built by Schantz Organ Company of Ohio, was installed in 1996. Internationally known organist, David Higgs, played the inaugural concert. The organ contains 20 ranks of pipes, two keyboards and a pedalboard. It has an electro pneumatic action. Some pipes are wood, others of a metal alloy of tin and lead. There are 1157 pipes on the wind chests in the loft over the console.
			</p>
			<p>
				The memorial altar is dedicated to those who have served in the armed forces. The cross was designed by E. B. Lewis and memorializes, with four gold stars, the men from St. Mary&#8217;s who died in WWI. The reredos, above the altar, was designed by Sigismund Wolf of Carmel, and carved by Taiwanese craftsmen. The <em>Stations of the Cross</em> were gifts from the craftsmen to St. Mary&#8217;s.
			</p>
			<p>
				The Madonna at the rear of the church was hand carved in Italy and given in 1981 in memory or Erik Olinger. The Italian marble baptismal font was one of the first gifts to the church, donated in 1887 by Mrs. A.A. Taft of New York City.
			</p>
