			<h3>COPA&#0151;Walking Together</h3>
			<p>
				Walking Together is a founding member of Communities Organized for Relational Power in Action, an organization of churches and other institutions in Monterey and Santa Cruz Counties. COPA institutions are working together across religion, race, class, education and geography for the common good of our region. At its founding assembly on Pentecost 2003, COPA committed to work together on problems of housing, education, healthcare, safe communities, immigrant issues and economic opportunity. Walking Together meets monthly at St. Mary&#8217;s for planning, reflection, fellowship and action.
